%% ====================================================================
%% External functions
%% ====================================================================
start() ->
	event_loop().


%% ====================================================================
%% Internal functions
%% ====================================================================
event_loop() ->
	receive
		{state_inf, chgui,_Parameters} ->
			event_loop();
		{'EXIT', Pid, Why} ->
			io:format("child process[~p] terminated : ~p~n", [Pid, Why]),
			exit(error);
		_Any ->
			io:format("loadenv unknown type received : ~p~n", [_Any]),
			event_loop()
	end.

